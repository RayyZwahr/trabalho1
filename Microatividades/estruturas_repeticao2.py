texto = 'Olá, laço for' #variável no formato string

for item in texto:  #laço for percorrendo a string
    print('Caractere: ' + item)  #imprimime a letra

for numero in range(1, 11): #novo laço que percorre os numeros de 1 a 11
    print('Número do intervalo: ' + str(numero)) #imprime o numero correspondente a cada letra da string
